// Arithmetic Operators

public class ArithmeticOp {

    public static void main(String[] args) {
        // Addition Operator
        System.out.println("The sum is: " + (1 + 3));
        // Subtraction Operator
        int x = 3 - 1;
        System.out.println("The diff is: " + x + (10 - 2));
        // Multiplication Operator
        System.out.println("The product is: " + 3 * 2);
        // Division Operator
        System.out.println("The result is: " + 6 / 2);
        // Modulo Operator
        System.out.println("The result is: " + 6 % 4);
    }

}