// Literals are constant values that appear directly in a program.
// Literals in Java are synthetic representation of boolean, numeric, character, or string data.

public class Main {

    public static void main(String[] args) {
        String s = "Hello"; // String literals.
        System.out.println("This is some text");
        char c = 'a'; // Character Literals.
        System.out.println('b');
        int i = 123; // Integer Literals.
        System.out.println(156);
        float f = 14.356f; // Floating-point Literals.
        System.out.println(f);
        boolean b1 = true; // Boolean Literals.
        boolean b2 = false; // Boolean Literals.
        System.out.println(b1);
        System.out.println(b2);
    }

}