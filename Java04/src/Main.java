// Title: Type faster in Intellij
// To go to beginning of the file, press Page Up button; to go to the end, press Page Down button.
// To scroll up and down, use CTRL+(Up or Down) arrow.
// To go to another class, open the class in another window first, then use ALT+(Right arrow); or vice versa.

public class Main {
    public static void main(String[] args) {

        // Navigation
        // If you wanna move cursor to another point using keyboard, using CTRL+(Left arrow) to move word by word; or vice versa.
        // To go to the beginning of the line, press Home button; to go to the end, press End button.
        System.out.println("Hello world!");

        /* Selection:
        To select letter by letter, use SHIFT+(Right arrow); vice versa.
        To select word by word, use CTRL+SHIFT+(Right arrow); vice versa.
        To select whole line, press SHIFT+(END/HOME).
         */
        System.out.println("Big Kids Java Programming Course");

        /* Indentation: either use Tab key or go to the bottom-right and press "4 spaces" to open the 'Tabs and Indents' window.
        To go back after Tabbed indent, either use Backspace or SHIFT+Tab.
         */

        // Duplicating code: put cursor on the line, press CTRL+D. To cut the line, press CTRL+X.
        System.out.println("hello 1");
        System.out.println("hello 1");

        // Adding/Removing comments: Press CTRL+/ to comment single line, you can also select multiple lines with this.
        // To select multiple lines, press SHIFT+(Down arrow).
        System.out.println("hello 1");
        System.out.println("hello 2");

        // Moving a piece of code: select the lines, press SHIFT+ALT+(Up/Down arrow).
        System.out.println("hello 1");
        System.out.println("hello 2");

        // Selecting a code block: put the cursor at the brace and press CTRL+W.
    }
}