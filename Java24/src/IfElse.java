// Title: IF-ELSE statement -> An optional block of an if statement.

/* These statements in the else block are executed only if the boolean-expression evaluates to false.
if (boolean-expression)
    statements
else
    statements
 */

public class IfElse {

    public static void main(String[] args) {
        if (true)
            System.out.printf("Hello");
        else
            System.out.printf("Bye");
    }
}

// If-else statement: only one block will be executed.